def pick_resistors(value):
    # Создаем словарь, где ключи - это названия рядов номиналов сопротивлений, а значения - кортежи номинальных значений сопротивлений
    resistor_series = {
        'E6': (10, 15, 22, 33, 47, 68),  # Серия E6
        'E12': (10, 12, 15, 18, 22, 27, 33, 39, 47, 56, 68, 82),  # Серия E12
        'E24': (10, 11, 12, 13, 15, 16, 18, 20, 22, 24, 27, 30, 33, 36, 39, 43, 47, 51, 56, 62, 68, 75, 82, 91),  # Серия E24
        'E48': (10, 11, 12, 13, 15, 16, 18, 20, 22, 24, 27, 30, 33, 36, 39, 43, 47, 51, 56, 62, 68, 75, 82, 91,
                10.2, 11.3, 12.4, 13.7, 15, 16.2, 17.8, 19.6, 21.5, 23.7, 26.1, 28.7, 31.6, 34.8, 38.3, 42.2, 46.4,
                51.1, 56.2, 61.9, 68.1, 75, 82.5, 91),  # Серия E48
        'E96': tuple(range(100, 1000)),  # Серия E96
    }

    result = {}
    for series, values in resistor_series.items():
        # Для каждого ряда номиналов находим ближайшие значения к входному значению
        closest_values = tuple(filter(lambda x: abs(x - value) == min(map(lambda y: abs(y - value), values)), values))
        result[series] = closest_values

    return result if result else None

# Ручные тесты
# Выводим ближайшие номиналы сопротивлений для заданных значений
print(pick_resistors(112))
print(pick_resistors(549))

# Пример ручного теста:
# >>> pick_resistors(112)
# {'E6': (100,), 'E12': (120,), 'E24': (110,), 'E48': (110,), 'E96': (113,)}
# >>> 
# >>> pick_resistors(549)
# {'E6': (470,), 'E12': (560,), 'E24': (560,), 'E48': (536, 562), 'E96': (549,)}
