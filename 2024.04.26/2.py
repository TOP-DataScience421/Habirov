def deck():
    # Создаем генераторную функцию deck, которая генерирует все возможные карты в колоде
    suits = ['черви', 'бубны', 'пики', 'трефы']
    for suit in suits:
        for value in range(2, 15):
            # Генерируем карту, представленную в виде кортежа (значение, масть)
            yield (value, suit)

# Ручной тест
# Создаем колоду карт и выводим список карт, взятых с шагом 13, чтобы получить по одной карте из каждой масти
print(list(deck())[::13])

# Пример ручного теста:
# >>> list(deck())[::13]
# [(2, 'черви'), (2, 'бубны'), (2, 'пики'), (2, 'трефы')]