number = int(input('Введите число: '))
print('Следующее за числом', 
      number, 
      'число:', 
      number + 1 ,
      '\n', 
      '\bДля числа', 
      number, 
      'предыдущее число:', 
      number - 1
)

#Пример ввода: 20

#Пример вывода:
#Следующее за числом 20 число: 21
#Для числа 20 предыдущее число: 19